<?php
echo "<h2>ข้อ 2: PHP Math Functions (ฟังก์ชันคณิตศาสตร์)</h2>";

// ==========================================
// 1. pi() - ค่า PI
// ==========================================
echo "<h3>2.1 ฟังก์ชัน pi()</h3>";
echo "ค่า PI = " . pi() . "<br>";
echo "คำนวณพื้นที่วงกลม (รัศมี = 5): " . (pi() * 5 * 5) . "<br><br>";

// ==========================================
// 2. min() และ max()
// ==========================================
echo "<h3>2.2 ฟังก์ชัน min() และ max()</h3>";
$numbers = array(0, 150, 30, 20, -8, -200);
echo "ตัวเลข: " . implode(", ", $numbers) . "<br>";
echo "ค่าต่ำสุด (min): " . min($numbers) . "<br>";
echo "ค่าสูงสุด (max): " . max($numbers) . "<br><br>";

// เปรียบเทียบ 2 ค่า
echo "เปรียบเทียบ 10 กับ 20<br>";
echo "min(10, 20) = " . min(10, 20) . "<br>";
echo "max(10, 20) = " . max(10, 20) . "<br><br>";

// ==========================================
// 3. abs() - ค่าสัมบูรณ์
// ==========================================
echo "<h3>2.3 ฟังก์ชัน abs() - ค่าสัมบูรณ์</h3>";
$negative = -6.7;
$positive = 6.7;
echo "abs(-6.7) = " . abs($negative) . "<br>";
echo "abs(6.7) = " . abs($positive) . "<br>";
echo "abs(-100) = " . abs(-100) . "<br><br>";

// ==========================================
// 4. sqrt() - รากที่สอง
// ==========================================
echo "<h3>2.4 ฟังก์ชัน sqrt() - รากที่สอง</h3>";
echo "sqrt(64) = " . sqrt(64) . "<br>";
echo "sqrt(25) = " . sqrt(25) . "<br>";
echo "sqrt(2) = " . sqrt(2) . "<br>";
echo "sqrt(100) = " . sqrt(100) . "<br><br>";

// ==========================================
// 5. round() - ปัดเศษ
// ==========================================
echo "<h3>2.5 ฟังก์ชัน round() - ปัดเศษ</h3>";
echo "round(0.60) = " . round(0.60) . "<br>";
echo "round(0.49) = " . round(0.49) . "<br>";
echo "round(0.5) = " . round(0.5) . "<br>";
echo "round(5.555, 2) = " . round(5.555, 2) . " (ทศนิยม 2 ตำแหน่ง)<br><br>";

// ฟังก์ชันปัดเศษเพิ่มเติม
echo "<h3>2.6 ฟังก์ชันปัดเศษเพิ่มเติม</h3>";
echo "ceil(4.3) = " . ceil(4.3) . " (ปัดขึ้น)<br>";
echo "floor(4.7) = " . floor(4.7) . " (ปัดลง)<br><br>";

// ==========================================
// 6. rand() - สุ่มตัวเลข
// ==========================================
echo "<h3>2.7 ฟังก์ชัน rand() - สุ่มตัวเลข</h3>";
echo "เลขสุ่ม: " . rand() . "<br>";
echo "เลขสุ่มระหว่าง 1-10: " . rand(1, 10) . "<br>";
echo "เลขสุ่มระหว่าง 10-100: " . rand(10, 100) . "<br><br>";

// ทดสอบสุ่มหลายครั้ง
echo "<h3>2.8 ทดสอบสุ่มตัวเลข 5 ครั้ง (1-50)</h3>";
for ($i = 1; $i <= 5; $i++) {
    echo "ครั้งที่ $i: " . rand(1, 50) . "<br>";
}
echo "<br>";

// ==========================================
// 7. ฟังก์ชันคณิตศาสตร์เพิ่มเติม
// ==========================================
echo "<h3>2.9 ฟังก์ชันคณิตศาสตร์อื่นๆ</h3>";
echo "pow(2, 3) = " . pow(2, 3) . " (2 ยกกำลัง 3)<br>";
echo "exp(1) = " . exp(1) . " (e ยกกำลัง 1)<br>";
echo "log(10) = " . log(10) . " (ลอการิทึมธรรมชาติ)<br>";
echo "log10(100) = " . log10(100) . " (ลอการิทึมฐาน 10)<br><br>";

// ==========================================
// 8. ตัวอย่างการใช้งานจริง
// ==========================================
echo "<h3>2.10 ตัวอย่างการใช้งานจริง</h3>";

// คำนวณระยะทาง 2 จุด
$x1 = 0; $y1 = 0;
$x2 = 3; $y2 = 4;
$distance = sqrt(pow($x2 - $x1, 2) + pow($y2 - $y1, 2));
echo "ระยะทางระหว่าง (0,0) กับ (3,4) = " . $distance . " หน่วย<br>";

// คำนวณเฉลี่ย
$scores = array(85, 92, 78, 90, 88);
$average = array_sum($scores) / count($scores);
echo "คะแนนเฉลี่ย: " . round($average, 2) . "<br>";

// ทอนเหรียญ
$coin = rand(0, 1);
echo "ทอนเหรียญ: " . ($coin == 0 ? "หัว" : "ก้อย") . "<br>";

echo "<hr>";
?>